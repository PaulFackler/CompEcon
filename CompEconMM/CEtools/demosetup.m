set(0,'defaultLegendInterpreter','latex')
set(0,'defaultTextInterpreter','latex')
set(0,'defaultTextHorizontalAlignment','left')
set(0,'defaultTextVerticalAlignment','bottom')
set(0,'defaultTextFontSize',12)